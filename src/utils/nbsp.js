// Убираем висячие предлоги/союзы/частицы в русском тексте.
// Заменяем пробел ПОСЛЕ короткого слова на неразрывный пробел (U+00A0),
// чтобы короткое слово не висело в конце строки.

const SHORT_WORDS = [
  // предлоги 1 буква
  'в', 'с', 'к', 'у', 'о', 'а', 'и',
  // предлоги 2-3 буквы
  'во', 'со', 'ко', 'об', 'на', 'за', 'по', 'до', 'из', 'от', 'под', 'над', 'при', 'для', 'без', 'про', 'из-за', 'из-под',
  // союзы
  'но', 'да', 'ни', 'или', 'то', 'же', 'бы', 'ли', 'ведь', 'вот',
  // частицы / вспомогательные
  'не', 'ну',
  // местоимения короткие
  'я', 'мы', 'ты', 'вы', 'он', 'мне', 'нам', 'вам', 'им', 'ей', 'ею', 'его',
  // прочее
  'что', 'как', 'кто', 'чем', 'где', 'куда', 'если', 'чтобы', 'сам', 'сама', 'сами',
]

const escape = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
const pattern = new RegExp(
  '(^|[\\s(«"„—–-])(' + SHORT_WORDS.map(escape).join('|') + ')\\s+',
  'giu'
)

export function nbsp(input) {
  if (input == null) return input
  if (Array.isArray(input)) return input.map(nbsp)
  const str = String(input)
  return str.replace(pattern, (_m, before, word) => `${before}${word}\u00A0`)
}

export default nbsp
